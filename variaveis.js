/**
 * para declarar uma variavel eh usado os nomes (const, let) sendo o const para declarar variais que nao podem mudar
 * e let para variaveis que podem ser alteradas
 * Ex. const <nome_da_variavel> = <valor_da_variavel>
 */

const nome = "Henrique" //tipo = string (texto). Valor precisa ser declarado entre aspas
let idade = 25 //tipo = number (numero). Precisa ser um numero e nao usa aspas

console.log(nome) // usa a variavel nome para passar o valor do nome do usuario
console.log(idade) // usa a variavel idade para passar o valor da idade do usuario